#include "libft.h"
#include <stdio.h>
#include <string.h>

size_t	ft_strlcpy(const char *src, char *dest, size_t size)
{
	size_t	i;

	i = 0;
	if (size == i)
		return (ft_strlen(src));
	while (src[i] != '\0' && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (ft_strlen(src));
}
